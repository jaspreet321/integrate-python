#IMP - FOR loop does the same work as WHILE loop does, it's just that FOR loop is bit less complicated than WHILE

#Let's draft the list of fruits under variable 'fruits' & then print that list using FOR loop

fruits = ['banana', 'apple', 'pineapple']

for item in fruits:          #'item' is just the way in FOR loop to call the entire list                    
    print(item)              
